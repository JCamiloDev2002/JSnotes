// Variables

/*
 ## VARIABLES
 are values in your code that can represent differente values each time the code runs.
  */

firstname = "maike";
x = 2;

//can be assigned a new value while the code is running

firstname = "edward";
x = 7;

/*
 ## DECLARING VARIABLES
 - primero se declara la variable(usando let, var o const)
 - segundo llamamos a la variable y usar el nombre para cambiar su valor por uno nuevo
 */

let lastname = "maria";
lastname = "jacky"; //eso se le llama HARDCODE porque el valor no proviene de un recurso externo.

/*
 ## LET, VAR AND CONST 
 consist in 3 parts
 variable-definid keyword,a name, a value
 */

let nr1; 

var nr2;

const PI = 3.1416;

/*
 Let y var pueden cambiar su valor, solo se diferencian en su alcance
 let = block scope
 var = global scope

 const es para valores que solo se pueden asignar una sola vez
  */


